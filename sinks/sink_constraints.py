def calculate_reines15_mbh_local(mstar_msun, alpha=7.45, beta=1.05):
    return 10**alpha * (mstar_msun / 1e11) ** beta  # msun
def calculate_reines15_mbh_bulges(mstar_msun, alpha=8.95, beta=1.40):
    return 10**alpha * (mstar_msun / 1e11) ** beta  # msun

def calculate_bennert21_bmh2disp(disp, alpha=8.51, beta=4.55):
    return 10**alpha * (disp / 200) ** beta #msun

